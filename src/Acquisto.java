
public class Acquisto {

	//ATTRIBUTI
	private int codP;
	private int codC;
	private int quantita;
	
	//METODI
	public int getCodP() {
		return codP;
	}
	public void setCodP(int codP) {
		this.codP = codP;
	}
	public int getCodC() {
		return codC;
	}
	public void setCodC(int codC) {
		this.codC = codC;
	}
	public int getQuantita() {
		return quantita;
	}
	public void setQuantita(int quantita) {
		this.quantita = quantita;
	}
	
	
}

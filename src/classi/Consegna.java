package classi;

public class Consegna {

	//ATTRIBUTI
	private int codC;
	private int codA;
	private int codR;
	private int codCl;
	private float prezzo;
	private String dataO; 
	private String dataC;
	private String metodoP;
	private String codCarta;
	private String note;
	private String via;
	private String civico;
	
	//METODI
	public int getCodC() {
		return codC;
	}
	public void setCodC(int codC) {
		this.codC = codC;
	}
	public int getCodA() {
		return codA;
	}
	public void setCodA(int codA) {
		this.codA = codA;
	}
	public int getCodR() {
		return codR;
	}
	public void setCodR(int codR) {
		this.codR = codR;
	}
	public int getCodCl() {
		return codCl;
	}
	public void setCodCl(int codCl) {
		this.codCl = codCl;
	}
	public float getPrezzo() {
		return prezzo;
	}
	public void setPrezzo(float prezzo) {
		this.prezzo = prezzo;
	}
	public String getDataO() {
		return dataO;
	}
	public void setDataO(String dataO) {
		this.dataO = dataO;
	}
	public String getDataC() {
		return dataC;
	}
	public void setDataC(String dataC) {
		this.dataC = dataC;
	}
	public String getMetodoP() {
		return metodoP;
	}
	public void setMetodoP(String metodoP) {
		this.metodoP = metodoP;
	}
	public String getCodCarta() {
		return codCarta;
	}
	public void setCodCarta(String codCarta) {
		this.codCarta = codCarta;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	public String getVia() {
		return via;
	}
	public void setVia(String via) {
		this.via = via;
	}
	public String getCivico() {
		return civico;
	}
	public void setCivico(String civico) {
		this.civico = civico;
	}
	
}

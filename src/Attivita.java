
public class Attivita {

	//ATTRIBUTI
	private int codA;
	private String orariA;
	private String via;
	private String civico;
	
	//METODI
	public int getCodA() {
		return codA;
	}
	public void setCodA(int codA) {
		this.codA = codA;
	}
	public String getOrariA() {
		return orariA;
	}
	public void setOrariA(String orariA) {
		this.orariA = orariA;
	}
	public String getVia() {
		return via;
	}
	public void setVia(String via) {
		this.via = via;
	}
	public String getCivico() {
		return civico;
	}
	public void setCivico(String civico) {
		this.civico = civico;
	}
	
	
}
